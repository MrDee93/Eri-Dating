import UIKit

extension UIColor {
    static func getRed() -> UIColor {
        
        return UIColor.init(red: 255/255, green: 33/256, blue: 0/255, alpha: 1.0)
    }
    
    static func getYellow() -> UIColor {
        return UIColor.init(red: 255/255, green: 255/255, blue: 0/255, alpha: 1.0)
    }
    
    static func getGreen() -> UIColor {
        return UIColor.init(red: 16/255, green: 242/255, blue: 50/255, alpha: 1.0)
    }
    
    static func getBlue() -> UIColor {
        return UIColor.init(red: 16/255, green: 133/255, blue: 242/255, alpha: 1.0)
    }
    
}
